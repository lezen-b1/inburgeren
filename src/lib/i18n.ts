function detectLocale(): Locale {
  if (typeof navigator === 'undefined') return 'nl';
  for (const language of navigator.languages.length ? navigator.languages : [navigator.language]) {
    const key = language.toLocaleLowerCase().split('-')[0];
    const mapped = aliases[key];
    if (mapped && supportedLocales.includes(mapped)) return mapped;
  }
  return 'nl';
}

export function useI18n() {
  const locale = useMemo(() => detectLocale(), []);
  const dictionary = dictionaries[locale] ?? dictionaries.nl;

  useEffect(() => {
    document.documentElement.lang = locale;
    document.documentElement.dir = rtlLocales.has(locale) ? 'rtl' : 'ltr';
  }, [locale]);

  return {
    locale,
    dir: rtlLocales.has(locale) ? 'rtl' : 'ltr',
    t(key: string, values?: Record<string, string | number>) {
      const template = dictionary[key] ?? dictionaries.nl[key] ?? key;
      if (!values) return template;
      return Object.entries(values).reduce((text, [name, value]) => text.replaceAll(`{${name}}`, String(value)), template);
    },
    skillLabel(skill: string) {
      const key = skill === 'cause-effect'
        ? 'skill_causeEffect'
        : skill === 'grammar-transform'
          ? 'skill_grammarTransform'
          : skill === 'inference-summary'
            ? 'skill_inferenceSummary'
            : `skill_${skill}`;
      return dictionary[key] ?? dictionaries.nl[key] ?? skill;
    },
    levelLabel(level: string) {
      return dictionary[`level_${level}`] ?? dictionaries.nl[`level_${level}`] ?? level;
    },
  };
}
